/* 
contoh syntax:
- pem-variabelan
    variable pada js itu teridiri dari 3 metode pemvariabelan. antaralain:
    - menggunakan keyword "var"
        contoh: var luas = 500; // bisa diubah(sifatnya var bisa dideklarasikan di akhir, meski nama variable dipanggil sebelum pemvariabelan tsb)
    - menggunakan keyword "let"
        contoh: let luas = 500; // bisa diubah(sifatnya wajib di deklarasikan terlebih dahulu sebelum dipanggil)
    - menggunakan keyword "const"
        contoh: const luas = 500; // nilai constan tidak dapat diubah
*/