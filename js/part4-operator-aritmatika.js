/* 
Operator Aritmatika
 => yang digunakan untuk operasi matematika (Angka)
 Terdiri Beberapa Jenis:
 - Penambahan/Addition/Penjumlahan menggunakan tanda plus (+)
 - Pengurangan menggunakan tanda minus (-)
 - Perkalian menggunakan tanda bintang (*)
 - Pembagian menggunakan tanda (/)
 - Sisa Bagi menggunakan tanda (%)
*/

// Contoh Penambahan menggunakan (+):
const A = 10;
const B = 5;
const C = A + B; // + merupakan sebuah operator aritmatika
document.body.innerHTML = C; // 15

// Contoh Pengurangan menggunakan tanda minus (-):
const D = 10;
const E = 5;
const F = D - E;
document.body.innerHTML = F; // 5

// Contoh Perkalian menggunakan tanda (*):
const G = 10;
const H = 5;
const I = G * H;
document.body.innerHTML = I; // 50

// Contoh Pembagian (/)
const J = 10;
const K = 5;
const L = J / K;
document.body.innerHTML = L; // 2

// Contoh mencari sisa bagi menggunakan tanda (%)
const M = 10;
const N = 5;
const O = M % N;
document.body.innerHTML = O; // 0 (NOL)