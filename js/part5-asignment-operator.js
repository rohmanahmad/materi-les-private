/* 
    Asignment Operator Yang Berarti (operator penugasan),
        dimana suatu nilai akan diberikan kepada suatu variabel
        dengan menambahkan operasi aritmatika pada variabel tersebut

    Operasi Aritmatika yg diperbolehkan yaitu:
        - = (sama dengan)
        - += (nilai pd "variable assigment" akan ditambahkan dengan variabel setelahnya)
            ex: a += b (nilai "a" adalah nilai dari "a" + "b")
        - -= (nilai pd "variable assigment" akan dikurangkan dengan variabel setelahnya)
            ex: a -= b (nilai "a" adalah nilai dari "a" - "b")
        - *= (nilai pd "variable assigment" akan dikalikan dengan variabel setelahnya)
            ex: a *= b (nilai "a" adalah nilai dari "a" * "b")
        - *= (nilai pd "variable assigment" akan dibagi dengan variabel setelahnya)
            ex: a /= b (nilai "a" adalah nilai dari "a" / "b")
        - %= (nilai pd "variable assigment" akan disisabagikan dengan variabel setelahnya)
            ex: a /= b (nilai "a" adalah nilai dari "a" % "b")
        - **= (nilai pd "variable assigment" akan dipangkatkan dengan variabel setelahnya)
            ex: a **= b (nilai "a" adalah nilai dari "a" ^ "b")
*/

/* Contoh pada operasi (+=) */
let x = 10;
let y = 5;
x += y; // x = (x + y)

/* Contoh pada operasi (+=) */
let xx = 10;
let yy = 5;
xx -= yy; // xx = (xx - yy)

